use std::collections::HashMap;

use crate::utils::error::{CliError, Result};

#[derive(Debug, Clone)]
pub enum SpecialVarType {
    Boolean,
    Enum { values: Vec<String> },
    Number { min: i64, max: i64 },
    String,
}

#[derive(Debug, Clone)]
pub struct SpecialVariable {
    pub name: String,
    pub default_value: String,
    pub description: String,
    pub var_type: SpecialVarType,
}

impl SpecialVariable {
    pub fn new(name: &str, default: &str, var_type: SpecialVarType, desc: &str) -> Self {
        Self {
            name: name.to_string(),
            default_value: default.to_string(),
            description: desc.to_string(),
            var_type,
        }
    }

    pub fn validate(&self, value: &str) -> Result<String> {
        match &self.var_type {
            SpecialVarType::Boolean => {
                let lower = value.to_lowercase();
                match lower.as_str() {
                    "on" | "true" | "yes" | "1" => Ok("on".to_string()),
                    "off" | "false" | "no" | "0" => Ok("off".to_string()),
                    _ => Err(CliError::Other(format!(
                        "Invalid boolean value '{}' for {}. Use on/off.",
                        value, self.name
                    ))),
                }
            }
            SpecialVarType::Enum { values } => {
                let lower = value.to_lowercase();
                if values.iter().any(|v| v.to_lowercase() == lower) {
                    Ok(lower)
                } else {
                    Err(CliError::Other(format!(
                        "Invalid value '{}' for {}. Valid values: {}",
                        value,
                        self.name,
                        values.join(", ")
                    )))
                }
            }
            SpecialVarType::Number { min, max } => {
                let num: i64 = value.parse().map_err(|_| {
                    CliError::Other(format!(
                        "Invalid number '{}' for {}. Must be {}-{}.",
                        value, self.name, min, max
                    ))
                })?;
                if num < *min || num > *max {
                    return Err(CliError::Other(format!(
                        "Value {} out of range for {}. Must be {}-{}.",
                        num, self.name, min, max
                    )));
                }
                Ok(value.to_string())
            }
            SpecialVarType::String => Ok(value.to_string()),
        }
    }
}

