#[derive(Debug, Clone)]
pub struct VariableStore {
    user_vars: HashMap<String, String>,
    special_vars: HashMap<String, SpecialVariable>,
    special_values: HashMap<String, String>,
}

impl Default for VariableStore {
    fn default() -> Self {
        Self::new()
    }
}

impl VariableStore {
    pub fn new() -> Self {
        let specials = define_special_variables();
        let mut special_vars = HashMap::new();
        let mut special_values = HashMap::new();

        for sv in specials {
            let default = sv.default_value.clone();
            special_values.insert(sv.name.clone(), default);
            special_vars.insert(sv.name.clone(), sv);
        }

        Self {
            user_vars: HashMap::new(),
            special_vars,
            special_values,
        }
    }

    pub fn set(&mut self, name: String, value: String) -> Result<()> {
        if self.special_vars.contains_key(&name) {
            let sv = self
                .special_vars
                .get(&name)
                .cloned()
                .expect("checked above");
            let validated = sv.validate(&value)?;
            self.special_values.insert(name, validated);
        } else {
            self.user_vars.insert(name, value);
        }
        Ok(())
    }

    pub fn get(&self, name: &str) -> Option<&String> {
        if self.special_values.contains_key(name) {
            self.special_values.get(name)
        } else {
            self.user_vars.get(name)
        }
    }

    pub fn remove(&mut self, name: &str) -> bool {
        if self.special_vars.contains_key(name) {
            if let Some(sv) = self.special_vars.get(name) {
                self.special_values
                    .insert(name.to_string(), sv.default_value.clone());
            }
            true
        } else {
            self.user_vars.remove(name).is_some()
        }
    }

    pub fn is_special(&self, name: &str) -> bool {
        self.special_vars.contains_key(name)
    }

    pub fn user_variables(&self) -> &HashMap<String, String> {
        &self.user_vars
    }

    pub fn special_variables(&self) -> &HashMap<String, SpecialVariable> {
        &self.special_vars
    }

    pub fn special_values(&self) -> &HashMap<String, String> {
        &self.special_values
    }

    pub fn all_variables(&self) -> Vec<(&String, &String)> {
        let mut all: Vec<(&String, &String)> = Vec::new();
        for (k, v) in &self.special_values {
            all.push((k, v));
        }
        for (k, v) in &self.user_vars {
            all.push((k, v));
        }
        all.sort_by(|a, b| a.0.cmp(b.0));
        all
    }

    pub fn get_bool(&self, name: &str) -> bool {
        self.get(name)
            .map(|v| matches!(v.as_str(), "on" | "true" | "yes" | "1"))
            .unwrap_or(false)
    }

    pub fn get_special_var(&self, name: &str) -> Option<&String> {
        self.special_values.get(name)
    }

    pub fn substitute(&self, input: &str) -> Result<String> {
        let mut result = String::with_capacity(input.len());
        let chars: Vec<char> = input.chars().collect();
        let mut i = 0;

        while i < chars.len() {
            if chars[i] == ':' && i + 1 < chars.len() {
                if chars[i + 1] == ':' {
                    result.push(':');
                    i += 2;
                    continue;
                }

                if chars[i + 1] == '\'' {
                    let (replaced, new_i) = self.substitute_quoted(&chars, i + 2, '\'')?;
                    result.push_str(&replaced);
                    i = new_i;
                    continue;
                }

                if chars[i + 1] == '"' {
                    let (replaced, new_i) = self.substitute_quoted(&chars, i + 2, '"')?;
                    result.push_str(&replaced);
                    i = new_i;
                    continue;
                }

                if is_var_start_char(chars[i + 1]) {
                    let (replaced, new_i) = self.substitute_plain(&chars, i + 1)?;
                    result.push_str(&replaced);
                    i = new_i;
                    continue;
                }
            }

            result.push(chars[i]);
            i += 1;
        }

        Ok(result)
    }

    fn substitute_plain(&self, chars: &[char], start: usize) -> Result<(String, usize)> {
        let mut end = start;
        while end < chars.len() && is_var_char(chars[end]) {
            end += 1;
        }

        let var_name: String = chars[start..end].iter().collect();

        if let Some(env_key) = var_name.strip_prefix("ENV_") {
            let value = std::env::var(env_key).map_err(|_| {
                CliError::Other(format!("Undefined environment variable: {}", env_key))
            })?;
            return Ok((value, end));
        }

        let value = self
            .get(&var_name)
            .ok_or_else(|| CliError::Other(format!("Undefined variable: :{}", var_name)))?;

        Ok((value.clone(), end))
    }

    fn substitute_quoted(
        &self,
        chars: &[char],
        start: usize,
        quote: char,
    ) -> Result<(String, usize)> {
        let mut end = start;
        while end < chars.len() && chars[end] != quote {
            end += 1;
        }

        if end >= chars.len() {
            return Err(CliError::Other(
                "Unterminated quoted variable reference".to_string(),
            ));
        }

        let var_name: String = chars[start..end].iter().collect();

        if let Some(env_key) = var_name.strip_prefix("ENV_") {
            let value = std::env::var(env_key).map_err(|_| {
                CliError::Other(format!("Undefined environment variable: {}", env_key))
            })?;
            return Ok((format!("{}{}{}", quote, value, quote), end + 1));
        }

        let value = self
            .get(&var_name)
            .ok_or_else(|| CliError::Other(format!("Undefined variable: :{}", var_name)))?;

        Ok((format!("{}{}{}", quote, value, quote), end + 1))
    }
}

fn is_var_start_char(c: char) -> bool {
    c.is_ascii_alphabetic() || c == '_'
}

fn is_var_char(c: char) -> bool {
    c.is_ascii_alphanumeric() || c == '_'
}
